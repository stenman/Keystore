CREATE SCHEMA KEYSTORE;

DROP TABLE KEYSTORE.credentials
IF EXISTS;
DROP TABLE KEYSTORE.configuration
IF EXISTS;

CREATE TABLE KEYSTORE.credentials (
  user_id    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  email_id   VARCHAR(50) NOT NULL,
  first_name VARCHAR(50) NOT NULL,
  last_name  VARCHAR(50) NOT NULL,
  password   INTEGER     NOT NULL
);

CREATE TABLE KEYSTORE.configuration (
  config_id    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  config_key   VARCHAR(255)                        NOT NULL,
  config_value VARCHAR(255),
  create_date  TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);